    private void btntestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntestActionPerformed
    count=comboexamid.getItemCount();
        if(counti==0)
    {
        JOptionPane.showMessageDialog(null,"Select the subject first");
    }
    
    else if(count==0)
    {
        JOptionPane.showMessageDialog(null,"no test paper for this subject");
    }
    else
    {   
      examid=comboexamid.getSelectedItem().toString().trim();
      try
      {     int totalQuestion=ExamDAO.getQuestionCountByExam(examid);
        Exam ob=new Exam(examid,subjectname,totalQuestion);
              
        TakePaperFrame questionsFrame=new TakePaperFrame(ob);
      questionsFrame.setVisible(true);
          
        this.dispose();
      }
      catch(SQLException ex)
      {
          JOptionPane.showMessageDialog(null,"databse error");
      }
    }
           
    }//GEN-LAST:event_btntestActionPerformed

