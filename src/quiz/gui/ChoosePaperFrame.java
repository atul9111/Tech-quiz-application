    private void combosubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combosubjectActionPerformed
       comboexamid.removeAllItems();
        
            validinp=validateInput();
            if(validinp==false)
            {
                JOptionPane.showMessageDialog(null,"Please select the subject name");
                return;
            }
         try
         {
             
               String subject=subjectname;
              
                if(ExamDAO.isPaperSet(subject)==false)
                {
                    JOptionPane.showMessageDialog(null,"No ExamPaper Set for this subject");
                    return;
                }
               

                  ArrayList<String> examlist;
                    examlist = ExamDAO.getExamIdBySubject(UserProfile.getUsername(),subjectname);
                  if(examlist.isEmpty()){
                      JOptionPane.showMessageDialog(null,"you have already appeared for all exam for this subject");
                      return;
                      
                  }
                  for(String examid:examlist)
                      {
                     comboexamid.addItem(examid);
                      }
                  
               
             
           
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Edit Paper Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_combosubjectActionPerformed

